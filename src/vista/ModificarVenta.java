    public void cargarElementos(){
        try {
            /////// OBtiene elementos
        DaoProducto dao = new DaoProducto();
        lista_productos= dao.listarProductos();            
        List<String> lista_productos_str = new ArrayList<String>();
        for (Producto x : lista_productos) {
            lista_productos_str.add(String.valueOf(x.getCodigo()));
        }
        
         DaoCliente daoc = new DaoCliente();
        lista_clientes= daoc.listarClientes();            
        List<String> lista_clientes_str = new ArrayList<String>();
        for (Cliente x : lista_clientes) {
            lista_clientes_str.add(x.getRut());
        }
        
         DaoVendedor daov = new DaoVendedor();
        lista_vendedores= daov.listarVendedores();            
        List<String> lista_vendedores_str = new ArrayList<String>();
        for (Vendedor x : lista_vendedores) {
            lista_vendedores_str.add(x.getRut());
        }
        
///////// creacion comobox
        if (items_productos instanceof DefaultComboBoxModel) {
        DefaultComboBoxModel itempro = (DefaultComboBoxModel) items_productos;
        itempro.removeAllElements();
        itempro.addElement("");
            for (String value : lista_productos_str) {
                itempro.addElement(value);
            }
            //Object selectedItem = items_productos.getSelectedItem();
        }
        
        
         if (items_clientes instanceof DefaultComboBoxModel) {
        DefaultComboBoxModel itemcli = (DefaultComboBoxModel) items_clientes;
        itemcli.removeAllElements();
        itemcli.addElement("");
            for (String value : lista_clientes_str) {
                itemcli.addElement(value);
            }
            //Object selectedItem = items_productos.getSelectedItem();
        }
         
         
                  if (items_vendedores instanceof DefaultComboBoxModel) {
        DefaultComboBoxModel itemven = (DefaultComboBoxModel) items_vendedores;
        itemven.removeAllElements();
        itemven.addElement("");
            for (String value : lista_vendedores_str) {
                itemven.addElement(value);
            }
            //Object selectedItem = items_productos.getSelectedItem();
        }
/////////////////////////////
        } catch (Exception e) {
        }

        
    }
    
    
    
    
