    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
       try {
           
            String rutCli = String.valueOf(cboCliente.getSelectedItem());
            String rutVend = String.valueOf(cboVendedor.getSelectedItem());
            int codigoProd = Integer.parseInt(cboCodProd.getSelectedItem().toString());
            
            DaoVenta dao = new DaoVenta();
            Venta ven = dao.buscarVenta(rutCli, rutVend, codigoProd);

            if (cboCliente.getSelectedItem().equals("")||cboCodProd.getSelectedItem().equals("")||cboVendedor.getSelectedItem().equals("")) {
                JOptionPane.showMessageDialog(rootPane, " No se han encontrado los datos buscados");
            }else {
                jdcFechaVenta.setDate(ven.getFechaVenta());
                
                

                cboCliente.setEnabled(false);
                cboCodProd.setEnabled(false);
                cboVendedor.setEnabled(false);
                
                
                btnBuscar.setEnabled(false);
                btnModificar.setEnabled(true);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

